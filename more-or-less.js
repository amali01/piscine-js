// Function to add 1 to a number
function more(num){
    return num +1;
}

// Function to subtract 1 from a number
function less(num){
    return num -1;
}

// Function to add two numbers
function add(num1 ,num2){
    return num1 +num2;
}

// Function to subtract the second number from the first
function sub(num1 ,num2){
    return num1 -num2;
}
